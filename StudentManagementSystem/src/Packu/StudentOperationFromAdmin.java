    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
           String query ="SELECT * FROM `students` WHERE ID =?";
            pmt = con.prepareStatement(query);
            pmt.setInt(1, Integer.parseInt(id.getText()));
           
                   ResultSet rs = pmt.executeQuery();
                   if(rs.next()){
                       sn.setText(rs.getString("StudentName"));
                       fn.setText(rs.getString("Father Name"));
                       ad.setText(rs.getString("Address"));
                       pn.setText(rs.getString("Phone"));
                       String choose = rs.getString("Class");
                       switch(choose){
                           case "11":
                           cls.setSelectedIndex(1);
                           break;
                           case "12":
                               cls.setSelectedIndex(2);
                               break;
                       }
                       
                   }
                    else{
                       JOptionPane.showMessageDialog(this, "ID not found");
                   }

       }catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
     
    }                         
    }//GEN-LAST:event_jButton1ActionPerformed

