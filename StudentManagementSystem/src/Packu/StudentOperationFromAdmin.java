    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
try{

            String query ="UPDATE `students` SET `StudentName`=?,`Father Name`=?,`Address`=?,`Phone`=?,`Class`=? WHERE ID=?";
            pmt=con.prepareStatement(query);
            pmt.setString(6, id.getText());
            pmt.setString(1, sn.getText());
            pmt.setString(2, fn.getText());
            pmt.setString(3, ad.getText());
            pmt.setString(4, pn.getText());
            pmt.setString(5, (String) cls.getSelectedItem());
            pmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Update successfully submitted");

        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

